package com.sand.common.collection;

public class Geo {
    private String province;
    private String city;

    public String getProvince() {
        return province;
    }

    public Geo setProvince(String province) {
        this.province = province;
        return this;
    }

    public String getCity() {
        return city;
    }

    public Geo setCity(String city) {
        this.city = city;
        return this;
    }
}
